namespace GielinorAdventures.Input.Enumerations
{
    /// <summary>
    /// Mouse button state.
    /// </summary>
    public enum MouseButtonState
    {
        /// <summary>
        /// The mouse button was just pressed.
        /// </summary>
        Pressed,

        /// <summary>
        /// They mouse button was just released.
        /// </summary>
        Released,

        /// <summary>
        /// The mouse button is down.
        /// </summary>
        Down,

        /// <summary>
        /// The mouse button is up.
        /// </summary>
        Up
    }
}
